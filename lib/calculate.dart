class Calculate {
  /// adding two numbers.
  /// arguments: two numbers
  int add(int num1, int num2) {
    return num1 + num2;
  }

  /// subtracting two numbers.
  /// arguments: two numbers
  int sub(int num1, int num2) {
    return num1 - num2;
  }

  /// Find square
  /// arguments: one number
  int square(int squareNum) {
    return (squareNum * squareNum);
  }
}
